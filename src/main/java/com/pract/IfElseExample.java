package com.pract;

public class IfElseExample {
    public static void main(String[] args) {
        int a=1,b=2,c=3;
        if(a>b){
            System.out.println ("a value is : " +a);
        }else if(b>c){
            System.out.println ("b value is : " +b);
        }else{
            System.out.println ("c value is : " +c);
        }
    }
}
